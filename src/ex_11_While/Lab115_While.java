package ex_11_While;

public class Lab115_While {
    public static void main(String[] args) {
        int i =0;
        while(i < 10){
            System.out.println(i);
            i++;
        }
        // I , C , U
        int j = 10;
        for( ; j < 20; j++)
        {
            System.out.println(j);
        }
    }
}
