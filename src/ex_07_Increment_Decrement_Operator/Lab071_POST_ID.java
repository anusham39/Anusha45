package ex_07_Increment_Decrement_Operator;

public class Lab071_POST_ID {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(++a);
        System.out.println(a);

        // POST increment = print first and then increase

        System.out.println(a++);
        System.out.println(a);
    }
}
