package ex_07_Increment_Decrement_Operator;

public class Lab070_In_De_Op {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        int b = ++a;    // pre - increment
        System.out.println(a);
        System.out.println(b);

        //EXP and Result Table
        // Line No | a | Result b
        //  5  |  10 | NA

    }
}
