package ex_04_Operators;

public class Lab037_Operators_ArithmeticOperators {
    public static void main(String[] args) {

        //Arthmetic operators
        // + -> Addition
        // - -> subtraction
        // * -> multiplication
        // / -> divison
        // % -> modulus (Remainder)
        int a = 20;
        int b = 4;
        float c =3.0f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%c);

        System.out.println("a+b");
        System.out.println(a+b);
    }
}
